package org.sangraama.controller.clientprotocol;

public class PlayerDelta {
    private int dx = 0, dy = 0;

    public int getDx() {
        return dx;
    }

    public void setDx(int dx) {
        this.dx = dx;
    }

    public int getDy() {
        return dy;
    }

    public void setDy(int dy) {
        this.dy = dy;
    }
}
